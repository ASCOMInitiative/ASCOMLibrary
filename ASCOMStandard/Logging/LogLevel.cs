namespace ASCOM.Alpaca.Logging
{
    /// <summary>
    /// Log level enumeration
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// Trace level
        /// </summary>
        Trace = 0,
        /// <summary>
        /// Debug level
        /// </summary>
        Debug = 1,
        /// <summary>
        /// Information level
        /// </summary>
        Information = 2,
        /// <summary>
        /// Warning level
        /// </summary>
        Warning = 3,
        /// <summary>
        /// Error level
        /// </summary>
        Error = 4,
        /// <summary>
        /// Fatal level
        /// </summary>
        Fatal = 5
    };
}